void dht11(uint16_t *temperatura, uint16_t *umidade) {
  //variaveis para calculo
  uint16_t tempc, umidc;
  //Setar pino dht11 como output
  GPIO_InitTypeDef GPIO_InitStruct = {0};
  //Sinal nível 0
  HAL_GPIO_WritePin(dht11_GPIO_Port, dht11_Pin, GPIO_PIN_RESET);
  GPIO_InitStruct.Pin = dht11_Pin;
  GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
  GPIO_InitStruct.Pull = GPIO_NOPULL;
  GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_VERY_HIGH;
  HAL_GPIO_Init(dht11_GPIO_Port, &GPIO_InitStruct);


  //tempo minimo 18ms conforme datasheet
  HAL_Delay(20);
  //Sinal nível 1
  HAL_GPIO_WritePin(dht11_GPIO_Port, dht11_Pin, GPIO_PIN_SET);
  //confome datasheet depois disso o host se comporta como entrada
  GPIO_InitStruct.Pin = dht11_Pin;
  GPIO_InitStruct.Mode = GPIO_MODE_INPUT;
  GPIO_InitStruct.Pull = GPIO_NOPULL;
  GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_VERY_HIGH;
  //Setar timer2 para 0
  __HAL_TIM_SET_COUNTER(&htim2, 0);
  //Variaveis auxiliares
  uint16_t ler[2];//marcar os tempos em nível lógico alto
  uint16_t dados[42];
  uint8_t bits[40];
  uint16_t temph = 0;
  uint16_t umidadeh = 0;

  //Lógica para captura de dados em nível lógico alto
  for(int i =0; i < 42; i++) {
    while(HAL_GPIO_ReadPin(dht11_GPIO_Port, dht11_Pin) == GPIO_PIN_RESET);//quando for de 0 para 1
    ler[0] = __HAL_TIM_GET_COUNTER(&htim2);//marca o tempo inicial
    while(HAL_GPIO_ReadPin(dht11_GPIO_Port, dht11_Pin) == GPIO_PIN_SET);
    ler[1] = __HAL_TIM_GET_COUNTER(&htim2);//tempo final
    dados[i] = ler[1] - ler[0];//calculo do tempo em microsegundos em nivel alto
  }

  //definir bits conforme tepos datasheet
  for(int i = 0; i < 40; i++) {
    //se nivel 1 estiver entre 20 e 32us bit = 0
    if((dados[i + 2] >= 20) && (dados[i + 2] <= 32)) {
      bits[i] = 0;
      }
    //se estiver nivel 1 entre 65 e 75us bit = 1
    if((dados[i+2] >= 65) && (dados[i+2] <= 75)) {
      bits[i] = 1;
    }
  }

  //Calcular temperatura e umidade pelos bits
  for(int i = 0; i < 8; i++) {
    // umidade primeiros 16 bits da esquerda para direita e temperatura 16 bits depois e ulitmos 8 bits checksum
    temph += bits[i+16] << (7 - i);
    umidadeh += bits[i] << (7 - i);
  }

  //Atribuicao de variaveis
  tempc = temph;
  umidc = umidadeh;
  *temperatura = tempc;
  *umidade = umidc;
}
